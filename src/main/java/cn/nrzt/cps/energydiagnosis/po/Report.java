package cn.nrzt.cps.energydiagnosis.po;


public class Report {
	private String monitorId;
	private String monitorName;
	private String deviceType;
	private String equipAddress;
	private String monitorDesc;
	private String buildingNo;
	private String floorNo;
	private String roomNo;
	private String deviceTypeNum;
	
	public String getMonitorId() {
		return monitorId;
	}
	public void setMonitorId(String monitorId) {
		this.monitorId = monitorId;
	}
	public String getMonitorName() {
		return monitorName;
	}
	public void setMonitorName(String monitorName) {
		this.monitorName = monitorName;
	}
	public String getDeviceType() {
		return deviceType;
	}
	public void setDeviceType(String deviceType) {
		this.deviceType = deviceType;
	}
	public String getEquipAddress() {
		return equipAddress;
	}
	public void setEquipAddress(String equipAddress) {
		this.equipAddress = equipAddress;
	}
	public String getMonitorDesc() {
		return monitorDesc;
	}
	public void setMonitorDesc(String monitorDesc) {
		this.monitorDesc = monitorDesc;
	}
	public String getBuildingNo() {
		return buildingNo;
	}
	public void setBuildingNo(String buildingNo) {
		this.buildingNo = buildingNo;
	}
	public String getFloorNo() {
		return floorNo;
	}
	public void setFloorNo(String floorNo) {
		this.floorNo = floorNo;
	}
	public String getRoomNo() {
		return roomNo;
	}
	public void setRoomNo(String roomNo) {
		this.roomNo = roomNo;
	}
	public String getDeviceTypeNum() {
		return deviceTypeNum;
	}
	public void setDeviceTypeNum(String deviceTypeNum) {
		this.deviceTypeNum = deviceTypeNum;
	}
}
